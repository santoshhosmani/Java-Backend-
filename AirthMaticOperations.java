import java.util.Scanner;

public class AirthMaticOperations {
    public static void main(String[] args){
        int a=6;
        int b=5;
        int c;
        int d;
        int e;
        int f;
        int g;
        // addition operation
        c=a+b;
        // subtraction operation
        d=a-b;
        // multiplication operation
        e=a*b;
        // divion operation
        f=a/b;
        // the modulus operation
        g=a%b;

        System.out.println("The addition value is:"+c);
        System.out.println("the subtraction value is:"+d);
        System.out.println("the multiplied value is:"+e);
        System.out.println("the divided value is:"+f);
        System.out.println("the modulus value is:"+g);
    }
}
